package academy.maze.HelpAlgorithm;

/**
 * Утилитный класс для проверки границ координат.
 * 
 * <p>Предоставляет методы для валидации того, что координаты находятся
 * в допустимых пределах массива или сетки.
 */
public class Bounds {
    /**
     * Проверяет, находятся ли координаты в допустимых пределах.
     * 
     * @param x координата X для проверки
     * @param y координата Y для проверки
     * @param rows количество строк (максимальное значение X)
     * @param cols количество столбцов (максимальное значение Y)
     * @return true, если координаты находятся в пределах [0, rows) x [0, cols)
     */
    public static boolean inBounds(int x, int y, int rows, int cols) {
        return x >= 0 && x < rows && y >= 0 && y < cols;
    }
}
