package academy.maze.Generate;

import academy.maze.dto.Maze;

/**
 * Интерфейс генератора лабиринтов.
 * 
 * <p>Определяет контракт для различных алгоритмов генерации лабиринтов.
 * Каждый генератор должен реализовать метод generate для создания лабиринта
 * заданного размера.
 */
public interface Generator {

    /**
     * Генерирует лабиринт заданного размера.
     * 
     * <p>Размеры указываются в количестве ячеек (не включая стены).
     * Результирующий лабиринт будет иметь размеры (width*2+1) x (height*2+1),
     * так как между ячейками находятся стены.
     *
     * @param width ширина лабиринта в ячейках
     * @param height высота лабиринта в ячейках
     * @return сгенерированный лабиринт
     * @throws IllegalArgumentException если невозможно сгенерировать лабиринт
     *                                  (например, неположительные размеры)
     */
    Maze generate(int width, int height);
}
